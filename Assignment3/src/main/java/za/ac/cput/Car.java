package za.ac.cput;

public class Car {

    private String nameOfCar;
    private String owner;

    public String getOwner() {
        return owner;
    }

    public Car(String nameOfCar) {
        this.nameOfCar = nameOfCar;
    }

    public void setNameOfCar(String nameOfCar) {
        this.nameOfCar = nameOfCar;
    }

    public String getNameOfCar() {
        return nameOfCar;
    }


}
